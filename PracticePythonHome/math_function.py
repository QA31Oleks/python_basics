import math

pi = -3.14
a = 5
x = 1
y = 2
z = 3

print(round(pi))
print(math.ceil(pi))
print(math.floor(pi))
print(abs(pi)) # абсолютне значення
print(pow(a, 3)) # зведення у ступінь
print(math.sqrt(a)) # квадратний корінь
print(max(x,y,z)) # шукає максимальне значення серед наведених
print(min(x,y,z)) # шукає мінімальне значення серед наведених